package zjs.sjjg;

public class Department {
    String name;
    String code;

    public Department(String name, String code) {
        this.name = name;
        this.code = code;
    }

    public Department() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        code = code;
    }
}
